// 1. Arithmetic Operators
// +, -, *, /, %, ** (exponentiation), ++ (increment), -- (decrement)
let a=10;
let b=20;
console.log("a + b = " + (a + b)); // Addition
console.log("a - b = " + (a - b)); // Subtraction
console.log("a * b = " + (a * b)); // Multiplication
console.log("a / b = " + (a / b)); // Division
console.log("a % b = " + (a % b)); // Modulus
console.log("a ** b = " + (a ** b)); // Exponentiation
console.log("a++ = " + (a++)); // Increment
console.log("++a = " + (++a)); // Increment
console.log("a-- = " + (a--)); // Decrement
console.log("--a = " + (--a)); // Decrement


// 2. Assignment Operators
// =, +=, -=, *=, /=, %=, **=
let c = 10;
console.log("c = " + c); // Assignment
console.log("c += 5 = " + (c += 5)); // Addition Assignment
console.log("c -= 5 = " + (c -= 5)); // Subtraction Assignment
console.log("c *= 5 = " + (c *= 5)); // Multiplication Assignment
console.log("c /= 5 = " + (c /= 5)); // Division Assignment
console.log("c %= 5 = " + (c %= 5)); // Modulus Assignment
console.log("c **= 5 = " + (c **= 5)); // Exponentiation Assignment


// 3. Comparison Operators
// ==, ===, !=, !==, >, <, >=, <=
let d = 10;
let e = "10";
console.log("d == e: " + (d == e)); // Loose Equality
console.log("d === e: " + (d === e)); // Strict Equality
console.log("d != e: " + (d != e)); // Loose Inequality
console.log("d !== e: " + (d !== e)); // Strict Inequality
console.log("d > e: " + (d > e)); // Greater Than
console.log("d < e: " + (d < e)); // Less Than
console.log("d >= e: " + (d >= e)); // Greater Than or Equal To
console.log("d <= e: " + (d <= e)); // Less Than or Equal To